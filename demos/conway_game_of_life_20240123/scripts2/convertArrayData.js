/**
 * Превращает ряды двумерного массива в колонки.
 * @param {Array<Array>} arr2D - исходный двумерный массив.
 * @return {Array<Array>} новый двумерный массив с преобразованными рядами в колонки.
 */
function convertArrayData(arr2D) {
    // Проверяем, пуст ли исходный массив, чтобы избежать ошибок при работе с пустыми массивами
    if (!arr2D.length) return [];

    // Инициализируем новый массив для хранения результатов
    const result = [];

    // Проходим по каждому элементу первого подмассива (предполагая, что все подмассивы одинаковой длины)
    for (let i = 0; i < arr2D[0].length; i++) {
        // Для каждого i создаем новый подмассив в результате
        const row = [];

        // Проходим по каждому подмассиву исходного массива
        for (let j = 0; j < arr2D.length; j++) {
            // Добавляем элемент из j-го подмассива на i-тую позицию в новом подмассиве
            row.push(arr2D[j][i]);
        }

        // Добавляем сформированный подмассив в результат
        result.push(row);
    }

    return result;
}

// Пример использования
const arr2D = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];

console.log(convertArrayData(arr2D));
// Выведет:
// [
//  [1, 4, 7],
//  [2, 5, 8],
//  [3, 6, 9]
// ]
